main(List<String> args) {
  int a = 10 + 5; // + 15
  a = 20 - 10; // - 10
  a = 10 * 2; // * 20
  double b = 10 / 2; //  / 5

  b = 10 % 3; // % 1 El sobrante de la division
  b = -b; // -expr Ahora seria negativo

  int c = 10 ~/ 3; // ~/ 3  la parte entera de una division

  int d = 1;

  d++; // ++ 2
  d--; // -- 1

  d += 2; // += 3
  d -= 2; // -= 1

  //tambien funciona con *=  /=
}
